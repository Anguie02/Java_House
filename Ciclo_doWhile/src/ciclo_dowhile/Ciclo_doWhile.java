//Primero ejecuta, déspues evalua la condición
package ciclo_dowhile;


public class Ciclo_doWhile {

    
    public static void main(String[] args) {
        //+++++++++
        int i = 1;
        /*La sentencia que se encuentra dentro de DO se ejecuta 
        independientement de una condicón, al menos una vez*/
        do{
            System.out.println(i);//imprime 1
            i ++;
        //Luego compara el numero
        }while(i<=10);
        
    
    }
      
        
      
    }
    

